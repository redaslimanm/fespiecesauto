import { writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import { detectPorts } from './detect-ports.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const { webPort, apiPort } = detectPorts()

const config = `# Auto-generated by scripts/generate-ngrok-config.mjs — do not edit manually.
# Regenerate: npm run ngrok:config
# Single tunnel: Vite proxies /api and /uploads to localhost:${apiPort}
version: "2"
tunnels:
  web:
    addr: ${webPort}
    proto: http
    schemes:
      - https
`

const configPath = join(root, 'ngrok.yml')
writeFileSync(configPath, config, 'utf8')
console.log(`Wrote ${configPath}`)
console.log(`  web → localhost:${webPort} (API proxied via Vite → localhost:${apiPort})`)

const output = {}

for (const category of categories) {
  if (category.slug === 'freinage') {
    output.freinage = freinage
    continue
  }

  const template = SUBCATEGORY_TEMPLATES[category.slug]
  if (!template) {
    console.warn(`Missing template for ${category.slug}`)
    continue
  }

  output[category.slug] = template.map(([slug, name, description]) => sub(slug, name, description))
}

const outFile = join(root, 'src/data/subcategories.json')
writeFileSync(outFile, `${JSON.stringify(output, null, 2)}\n`, 'utf-8')

const counts = Object.fromEntries(Object.entries(output).map(([k, v]) => [k, v.length]))
